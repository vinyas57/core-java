public class School {
    private int id;
    private String name;
    private String location;
 
    public void setName(String nos) {
        name = nos;
    }
    public void setLocation(String nob) {
        location = nob;
    }
	public void setid(int a){
		id = a;
	}

    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public String getLocation() {
        return location;
    }
}

